let x = 10;
let y = 7;
let z = 25;


// basic arithmetic operators
console.log( x + y );
console.log( x - y );
console.log( x * y );
console.log( x / y );
console.log( x % y );
console.log( x ** y );


// pre increment && pre decrease
console.log(x);
console.log(++x);
console.log(++x);
console.log(++x);
console.log(--x);
console.log(--x);
console.log(--x);


// post increment 
console.log(z++);
console.log(z++);
console.log(z++);
console.log(z--);
console.log(z--);
console.log(z--);
console.log(z);

// string concat
let a = "Phillip";
let b = "David";
let c = "Shields";

console.log("My name is " + a + " " + b + " " + c + ".");
console.log(`${a} ${b} ${c}`);

